char	*ft_strstr(const char *a, const char *b)
{
	int		i;
	int		u;

	i = 0;
	u = 0;
	if (b[u] == '\0')
		return ((char*)(a));
	while (a[i] != '\0')
	{
		if (a[i] != b[u])
			i++;
		else
		{
			while (a[i] == b[u] && a[i] != '\0' && b[u] != '\0')
			{
				i++;
				u++;
			}
			if (b[u] == '\0')
				return ((char*)a + (i - u));
			i = (i - u) + 1;
			u = 0;
		}
	}
	return (0);
}
